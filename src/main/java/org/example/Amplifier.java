package org.example;

// Subsystem Class 2: Amplifier
class Amplifier {
    // Method to turn on the amplifier
    public void turnOn() {
        System.out.println("Amplifier turned on");
    }

    // Method to select the Bluetooth input on the amplifier
    public void selectBluetoothInput() {
        System.out.println("Bluetooth input selected on the amplifier");
    }

    // Method to adjust the volume on the amplifier
    public void setVolume(int level) {
        System.out.println("Amplifier volume set to " + level);
    }
}
