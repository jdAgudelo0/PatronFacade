package org.example;

// Subsystem Class 3: BluRayPlayer
class BluRayPlayer {
    // Method to turn on the Blu-Ray player
    public void turnOn() {
        System.out.println("Blu-Ray player turned on");
    }

    // Method to play a movie on the Blu-Ray player
    public void playMovie(String movie) {
        System.out.println("Playing " + movie + " on the Blu-Ray player");
    }
}